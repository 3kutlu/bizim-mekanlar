    using p_user_id;
  end if;

  update public."PlaceNotes"
  set "IsActive" = false, "UpdatedDate" = now()
  where "UserId" = p_user_id and "IsActive" = true;

  update public."UserPlaceLists"
  set "IsActive" = false, "UpdatedDate" = now()
  where "UserId" = p_user_id and "IsActive" = true;

  update public."Users"
  set "ProfilePhotoPath" = null,
      "DeletionAuthUserId" = coalesce("DeletionAuthUserId", "AuthUserId"),
      "AuthUserId" = null,
      "UpdatedDate" = now()
  where "UserId" = p_user_id;
end;
