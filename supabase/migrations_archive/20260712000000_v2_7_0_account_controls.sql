-- Statement 38/85
create or replace function public."BlockUser"(p_blocked_user_id integer)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user_id integer := public."GetMyActiveUserId"();
begin
  if p_blocked_user_id is null or p_blocked_user_id = v_user_id then
    raise exception 'Geçersiz kullanıcı.';
  end if;

  if not exists (
    select 1
    from public."Users" u
    where u."UserId" = p_blocked_user_id
      and u."IsActive" = true
      and coalesce(u."AccountStatus", 'ACTIVE') = 'ACTIVE'
  ) then
    raise exception 'Kullanıcı bulunamadı.';
  end if;

  update public."UserBlocks"
  set "IsActive" = true, "UpdatedDate" = now()
  where "BlockerUserId" = v_user_id
    and "BlockedUserId" = p_blocked_user_id;

  if not found then
    insert into public."UserBlocks" (
      "BlockerUserId", "BlockedUserId", "IsActive", "CreatedDate"
    ) values (
      v_user_id, p_blocked_user_id, true, now()
    );
  end if;

  update public."UserMutes"
  set "IsActive" = false, "UpdatedDate" = now()
  where "IsActive" = true
    and (("MuterUserId" = v_user_id and "MutedUserId" = p_blocked_user_id)
      or ("MuterUserId" = p_blocked_user_id and "MutedUserId" = v_user_id));

  update public."UserFollows"
  set "IsActive" = false,
      "UpdatedDate" = now()
  where "IsActive" = true
    and (("FollowerUserId" = v_user_id and "FollowingUserId" = p_blocked_user_id)
      or ("FollowerUserId" = p_blocked_user_id and "FollowingUserId" = v_user_id));

  if to_regclass('public."PlaceNoteReactions"') is not null
     and exists (
       select 1
       from information_schema.columns
       where table_schema = 'public'
         and table_name = 'PlaceNoteReactions'
         and column_name in ('PlaceNoteId', 'UserId', 'IsActive')
       group by table_schema, table_name
       having count(*) = 3
     ) then
    execute $sql$
      update public."PlaceNoteReactions" reaction
      set "IsActive" = false,
          "UpdatedDate" = now()
      from public."PlaceNotes" note_row
      where reaction."PlaceNoteId" = note_row."PlaceNoteId"
        and reaction."IsActive" = true
        and ((reaction."UserId" = $1 and note_row."UserId" = $2)
          or (reaction."UserId" = $2 and note_row."UserId" = $1))
    $sql$ using v_user_id, p_blocked_user_id;
  end if;

  update public."Notifications"
  set "IsActive" = false
  where "IsActive" = true
    and (("RecipientUserId" = v_user_id and "ActorUserId" = p_blocked_user_id)
      or ("RecipientUserId" = p_blocked_user_id and "ActorUserId" = v_user_id));

  -- Collaborative collection schema changed during earlier releases. Handle the
  -- current UserPlaceListId/UserId layout only when those columns are present.
  if to_regclass('public."UserPlaceListCollaborators"') is not null
     and exists (
       select 1 from information_schema.columns
       where table_schema = 'public'
         and table_name = 'UserPlaceListCollaborators'
         and column_name = 'UserId'
     ) then
    execute $sql$
      update public."UserPlaceListCollaborators" c
      set "IsActive" = false
      from public."UserPlaceLists" l
      where c."UserPlaceListId" = l."UserPlaceListId"
        and c."IsActive" = true
        and ((l."UserId" = $1 and c."UserId" = $2)
          or (l."UserId" = $2 and c."UserId" = $1))
    $sql$ using v_user_id, p_blocked_user_id;
  end if;
end;
$$;

