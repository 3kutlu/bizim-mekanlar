create or replace function public."PurgeUserAccountData"(p_user_id integer)
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
  if not exists (
    select 1 from public."Users" u
    where u."UserId" = p_user_id
      and u."AccountStatus" = 'DELETION_PENDING'
      and u."ScheduledDeletionDate" <= now()
  ) then
    raise exception 'Silinmeye hazır hesap bulunamadı.';
  end if;

  update public."Notifications"
  set "IsActive" = false
  where "RecipientUserId" = p_user_id or "ActorUserId" = p_user_id;

  update public."UserFollows"
  set "IsActive" = false, "UpdatedDate" = now()
  where "FollowerUserId" = p_user_id or "FollowingUserId" = p_user_id;

  delete from public."UserMutes"
  where "MuterUserId" = p_user_id or "MutedUserId" = p_user_id;

  delete from public."UserBlocks"
  where "BlockerUserId" = p_user_id or "BlockedUserId" = p_user_id;

  if to_regclass('public."WebPushSubscriptions"') is not null then
    execute 'update public."WebPushSubscriptions" set "IsActive" = false, "UpdatedDate" = now() where "UserId" = $1'
    using p_user_id;
  end if;

  if to_regclass('public."UserWebPushPreferences"') is not null then
    execute 'delete from public."UserWebPushPreferences" where "UserId" = $1'
    using p_user_id;
  end if;

  if to_regclass('public."PlaceNoteReactions"') is not null then
    execute 'update public."PlaceNoteReactions" set "IsActive" = false, "UpdatedDate" = now() where "UserId" = $1'
    using p_user_id;
  end if;

  if to_regclass('public."UserPlaceListCollaborators"') is not null
     and exists (
       select 1 from information_schema.columns
       where table_schema = 'public'
         and table_name = 'UserPlaceListCollaborators'
         and column_name = 'UserId'
     ) then
    execute 'update public."UserPlaceListCollaborators" set "IsActive" = false where "UserId" = $1'
    using p_user_id;
  end if;

  update public."PlaceNotes"
  set "IsActive" = false, "UpdatedDate" = now()
  where "UserId" = p_user_id and "IsActive" = true;

  update public."UserPlaceLists"
  set "IsActive" = false, "UpdatedDate" = now()
  where "UserId" = p_user_id and "IsActive" = true;

  update public."Users"
  set "ProfilePhotoPath" = null,
      "DeletionAuthUserId" = coalesce("DeletionAuthUserId", "AuthUserId"),
      "AuthUserId" = null,
      "UpdatedDate" = now()
  where "UserId" = p_user_id;
end;
$$;

