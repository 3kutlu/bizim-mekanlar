create or replace function public."ApplyNotificationRelationshipRules"()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  if not coalesce(new."IsActive", true)
     or new."RecipientUserId" is null
     or new."ActorUserId" is null then
    return new;
  end if;

  if exists (
    select 1
    from public."UserBlocks" b
    where b."IsActive" = true
      and ((b."BlockerUserId" = new."RecipientUserId" and b."BlockedUserId" = new."ActorUserId")
        or (b."BlockerUserId" = new."ActorUserId" and b."BlockedUserId" = new."RecipientUserId"))
  ) then
    new."IsActive" := false;
    return new;
  end if;

  if new."NotificationTypeCode" in (
      'FOLLOWING_NOTE',
      'NOTE_REACTION_UP',
      'NOTE_REACTION_DOWN'
    ) and exists (
      select 1
      from public."UserMutes" m
      where m."MuterUserId" = new."RecipientUserId"
        and m."MutedUserId" = new."ActorUserId"
        and m."IsActive" = true
    ) then
    new."IsActive" := false;
  end if;

  return new;
end;
$$;

