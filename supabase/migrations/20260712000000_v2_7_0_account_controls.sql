create or replace function public."MuteUser"(p_muted_user_id integer)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user_id integer := public."GetMyActiveUserId"();
begin
  if p_muted_user_id is null or p_muted_user_id = v_user_id then
    raise exception 'Geçersiz kullanıcı.';
  end if;

  if exists (
    select 1 from public."UserBlocks" b
    where b."IsActive" = true
      and ((b."BlockerUserId" = v_user_id and b."BlockedUserId" = p_muted_user_id)
        or (b."BlockerUserId" = p_muted_user_id and b."BlockedUserId" = v_user_id))
  ) then
    raise exception 'Engellenmiş kullanıcı sessize alınamaz.';
  end if;

  if not exists (
    select 1
    from public."GetExternalUserProfile"(p_muted_user_id) as target_profile
    where target_profile."FollowStatusCode" = 'ACCEPTED'
  ) then
    raise exception 'Yalnızca takip ettiğin kullanıcıları sessize alabilirsin.';
  end if;

  update public."UserMutes"
  set "IsActive" = true,
      "CreatedDate" = now(),
      "UpdatedDate" = now()
  where "MuterUserId" = v_user_id
    and "MutedUserId" = p_muted_user_id;

  if not found then
    insert into public."UserMutes" (
      "MuterUserId", "MutedUserId", "IsActive", "CreatedDate"
    ) values (
      v_user_id, p_muted_user_id, true, now()
    );
  end if;

  update public."Notifications"
  set "IsActive" = false
  where "RecipientUserId" = v_user_id
    and "ActorUserId" = p_muted_user_id
    and "IsActive" = true
    and "NotificationTypeCode" in (
      'FOLLOWING_NOTE',
      'NOTE_REACTION_UP',
      'NOTE_REACTION_DOWN'
    );
end;
$$;

create or replace function public."UnmuteUser"(p_muted_user_id integer)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user_id integer := public."GetMySessionUserId"();
  v_muted_since timestamptz;
begin
  select m."CreatedDate"
  into v_muted_since
  from public."UserMutes" m
  where m."MuterUserId" = v_user_id
    and m."MutedUserId" = p_muted_user_id
    and m."IsActive" = true
  order by m."CreatedDate" desc
  limit 1;

  update public."UserMutes"
  set "IsActive" = false, "UpdatedDate" = now()
  where "MuterUserId" = v_user_id
    and "MutedUserId" = p_muted_user_id
    and "IsActive" = true;

  if v_muted_since is not null then
    update public."Notifications"
    set "IsActive" = false
    where "RecipientUserId" = v_user_id
      and "ActorUserId" = p_muted_user_id
      and "CreatedDate" >= v_muted_since
      and "NotificationTypeCode" in (
        'FOLLOWING_NOTE',
        'NOTE_REACTION_UP',
        'NOTE_REACTION_DOWN'
      );
  end if;
end;
$$;

