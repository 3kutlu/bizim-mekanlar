create or replace function public."EnforceUserFollowRelationshipRules"()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  if coalesce(new."IsActive", true) and exists (
    select 1
    from public."UserBlocks" b
    where b."IsActive" = true
      and ((b."BlockerUserId" = new."FollowerUserId" and b."BlockedUserId" = new."FollowingUserId")
        or (b."BlockerUserId" = new."FollowingUserId" and b."BlockedUserId" = new."FollowerUserId"))
  ) then
    raise exception 'Engellenmiş kullanıcıyla takip ilişkisi kurulamaz.';
  end if;

  return new;
end;
$$;

