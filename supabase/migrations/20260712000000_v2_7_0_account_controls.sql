language plpgsql
security definer
set search_path = public
as $$
begin
  update public."Users"
  set "AuthUserId" = null,
      "DeletionAuthUserId" = null,
      "Username" = left('deleted_' || "UserId"::text || '_' || substr(md5(random()::text), 1, 8), 30),
      "Email" = 'deleted+' || "UserId"::text || '@invalid.local',
      "FirstName" = 'Silinmiş Hesap',
      "LastName" = null,
      "ProfilePhotoPath" = null,
      "AccountStatus" = 'DELETED',
      "IsActive" = false,
      "FrozenDate" = null,
      "DeletionRequestedDate" = null,
      "ScheduledDeletionDate" = null,
      "UpdatedDate" = now()
  where "UserId" = p_user_id;
end;
$$;

-- A user can only read their own relationship rows. All writes go through RPCs.
drop policy if exists "UserBlocks_SelectOwn" on public."UserBlocks";
create policy "UserBlocks_SelectOwn"
on public."UserBlocks"
for select
to authenticated
using (
  "BlockerUserId" = public."GetMySessionUserId"()
  or "BlockedUserId" = public."GetMySessionUserId"()
);

drop policy if exists "UserMutes_SelectOwn" on public."UserMutes";
create policy "UserMutes_SelectOwn"
on public."UserMutes"
for select
to authenticated
using ("MuterUserId" = public."GetMySessionUserId"());

revoke all on table public."UserBlocks" from anon, authenticated;
revoke all on table public."UserMutes" from anon, authenticated;
grant select on table public."UserBlocks" to authenticated, service_role;
grant select on table public."UserMutes" to authenticated, service_role;
grant all on table public."UserBlocks" to service_role;
grant all on table public."UserMutes" to service_role;
