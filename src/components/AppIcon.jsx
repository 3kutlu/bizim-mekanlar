export default function AppIcon({
  name,
  className = "",
  title = "",
  decorative = true,
}) {
  const rawName = String(name ?? "").trim();
  const normalizedName = LEGACY_ICON_MAP[rawName] || rawName;
  const source = ICONS[normalizedName] || ICONS["warning"];

  return (
    <img
      className={`app-icon${className ? ` ${className}` : ""}`}
      src={source}
      alt={decorative ? "" : title || rawName || "İkon"}
      aria-hidden={decorative ? "true" : undefined}
      title={title || undefined}
    />
  );
}

export function CollectionIcon({ value, className = "", title = "" }) {
  const rawValue = String(value ?? "").trim();
  const iconName = LEGACY_ICON_MAP[rawValue] || rawValue || "bookmarks";

  return <AppIcon name={iconName} className={className} title={title} />;
}
